"""
4. 使用循环输出菱形 例如用户输入 7（用户输入偶数，则提示不能打印〉 输出如下结果
   *
  ***
 *****
*******
 *****
  ***
   *
"""
n = int(input('输入一个奇数：'))
if n % 2 != 0:
    for i in range(1, n+1):
        if i < (n+1) / 2:
            print(' '*((n+1)//2-i), end='')
            print('*'*(2*i-1))
        elif i == (n+1) / 2:
            print('*'*(2*i-1))
        else:
            print(' '*(i-(n+1)//2), end='')
            print('*'*(2*(n-i)+1))
else:
    print('用户输入的是偶数，不能打印！')
